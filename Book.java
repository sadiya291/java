package com.nucleus.book;

public class Book {
	private String Book_Id;
	private String Book_Name;
	private long ISBN_No;
	private long Price;
	private String Authors_Name;
	private String Category_of_book;
	private String Publication_Name;
	private String Book_Description;
	
	
	
	
	public String getBook_Id() {
		return Book_Id;
	}
	public void setBook_Id(String book_Id) {
		Book_Id = book_Id;
	}
	public String getBook_Name() {
		return Book_Name;
	}
	public void setBook_Name(String book_Name) {
		Book_Name = book_Name;
	}
	public long getISBN_No() {
		return ISBN_No;
	}
	public void setISBN_No(long iSBN_No) {
		ISBN_No = iSBN_No;
	}
	public long getPrice() {
		return Price;
	}
	public void setPrice(long price) {
		Price = price;
	}
	public String getAuthors_Name() {
		return Authors_Name;
	}
	public void setAuthors_Name(String authors_Name) {
		Authors_Name = authors_Name;
	}
	public String getCategory_of_book() {
		return Category_of_book;
	}
	public void setCategory_of_book(String category_of_book) {
		Category_of_book = category_of_book;
	}
	public String getPublication_Name() {
		return Publication_Name;
	}
	public void setPublication_Name(String publication_Name) {
		Publication_Name = publication_Name;
	}
	public String getBook_Description() {
		return Book_Description;
	}
	public void setBook_Description(String book_Description) {
		Book_Description = book_Description;
	}
	

}
